def pluck(data: dict, path: str, default="None"):
    if path in data:
        return data[path]

    path = path.split(".")
    for _ in data:
        try:
            if isinstance(data[path[0]], dict):
                data = data[path[0]]
                del path[0]
                return pluck(data, ".".join(path), default)
        except KeyError:
            return default


